    public void populateRoleCombo() throws IOException {

        cmbRoles.removeAllItems();
        AdminUI.AdminDB admin = new AdminUI.AdminDB(workArea, department);
       StudentDetail student = new StudentDetail(workArea, department, selectedID);

        cmbRoles.addItem(admin);
        cmbRoles.addItem(student);

    }

    public void populateStudentBox() {

        lblStudentID.setText("Enter Student ID");
        txtStudentID.setVisible(true);

    }

    private void updateRolesVisibility() {
        
        warningForValidId.setText("");
        if ((cmbRoles.getSelectedItem() == null) || (cmbRoles.getSelectedItem().getClass() == AdminUI.AdminDB.class)) {
            selectedID = null;
            lblStudentID.setVisible(false);
            txtStudentID.setVisible(false);
            return;
        } else {
            lblStudentID.setVisible(true);
            lblStudentID.setText("Enter Student ID");
            txtStudentID.setVisible(true);
        }

    }
