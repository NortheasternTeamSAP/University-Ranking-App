    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:

        JPanel selectedPanel = (JPanel) cmbRoles.getSelectedItem();

        if (selectedPanel.getClass() == StudentDetail.class) {
            selectedID = department.getStudentDirectory().findStudent(txtStudentID.getText());
            if (selectedID != null) {
                warningForValidId.setText("");
                try {
                    selectedPanel = new StudentDetail(workArea, department, selectedID);
                } catch (IOException ex) {
                    Logger.getLogger(jLoginPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
                txtStudentID.setText("");
            } else {
                txtStudentID.setText("");
                warningForValidId.setText("Please enter valid student Id");
                
            }
        } else if (selectedPanel.getClass() == AdminUI.AdminDB.class) {
            warningForValidId.setText("");
            try {
                selectedPanel = new AdminUI.AdminDB(workArea, department );
            } catch (IOException ex) {
                Logger.getLogger(jLoginPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        nextScreen(workArea, selectedPanel, "selectedPanel");
    }//GEN-LAST:event_btnLoginActionPerformed

